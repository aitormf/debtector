# ──────────────────────────────────────────────
# Inline / minimal Python snippets
# ──────────────────────────────────────────────


class TestPythonInlineSnippets:
    """Tests for small inline Python programs written to tmp files."""

    def test_function_without_docstring(self, tmp_path: Path) -> None:
        """Function with no docstring has docstring=None."""
        f = tmp_path / "nodoc.py"
        f.write_text("def plain():\n    return 1\n", encoding="utf-8")
        nodes, _ = PythonParser().parse(str(f))
        fn = next(n for n in nodes if n.name == "plain")
        assert fn.docstring is None

    def test_aliased_from_import_creates_edge(self, tmp_path: Path) -> None:
        """'from flask import Flask as App' creates IMPORTS_FROM edge for 'flask'."""
        f = tmp_path / "alias_from.py"
        f.write_text("from flask import Flask as App\n", encoding="utf-8")
        _, edges = PythonParser().parse(str(f))
        import_targets = {e.target for e in edges if e.kind == EdgeKind.IMPORTS_FROM}
        assert "flask" in import_targets

    def test_class_with_only_pass_no_docstring(self, tmp_path: Path) -> None:
        """A class whose body is just 'pass' has no docstring."""
        f = tmp_path / "passonly.py"
        f.write_text("class Empty:\n    pass\n", encoding="utf-8")
        nodes, _ = PythonParser().parse(str(f))
        cls = next(n for n in nodes if n.name == "Empty")
        assert cls.docstring is None

    def test_self_method_call_generates_calls_edge(self, tmp_path: Path) -> None:
        """self.validate() from within a method generates a CALLS edge."""
        f = tmp_path / "calls.py"
        f.write_text(
            "class Svc:\n"
            "    def run(self):\n"
            "        return self.validate()\n"
            "    def validate(self):\n"
            "        return True\n",
            encoding="utf-8",
        )
        _, edges = PythonParser().parse(str(f))
        calls = [e for e in edges if e.kind == EdgeKind.CALLS]
        assert any("run" in e.source and "validate" in e.target for e in calls)

    def test_class_method_call_same_class(self, tmp_path: Path) -> None:
        """Calling another function in the same class by name resolves to a CALLS edge."""
        f = tmp_path / "same_class.py"
        f.write_text(
            "class Svc:\n"
            "    def run(self):\n"
            "        return self.helper()\n"
            "    def helper(self):\n"
            "        return 42\n",
            encoding="utf-8",
        )
        _, edges = PythonParser().parse(str(f))
        calls = [e for e in edges if e.kind == EdgeKind.CALLS]
        assert any("run" in e.source and "helper" in e.target for e in calls)
