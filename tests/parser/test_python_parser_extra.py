# ──────────────────────────────────────────────
# Aliased imports — import X as Y
# ──────────────────────────────────────────────


class TestPythonAliasedImports:
    """Tests for IMPORTS_FROM edges generated by 'import X as Y' statements."""

    def test_aliased_import_creates_edge(self, parsed_extras) -> None:
        """import os as opsys creates an IMPORTS_FROM edge targeting 'os'."""
        _, edges = parsed_extras
        import_targets = {e.target for e in edges if e.kind == EdgeKind.IMPORTS_FROM}
        assert "os" in import_targets

    def test_aliased_import_target_is_original_name(self, parsed_extras) -> None:
        """The edge target is the original module name, not the alias."""
        _, edges = parsed_extras
        import_targets = {e.target for e in edges if e.kind == EdgeKind.IMPORTS_FROM}
        # The alias 'opsys' must NOT appear as an import target
        assert "opsys" not in import_targets

    def test_multiple_aliased_imports(self, parsed_extras) -> None:
        """Both 'import os as opsys' and 'import sys as system' create edges."""
        _, edges = parsed_extras
        import_targets = {e.target for e in edges if e.kind == EdgeKind.IMPORTS_FROM}
        assert "sys" in import_targets


